namespace ImprezGarage.Infrastructure
{
    /// <summary>
    /// All of the current region names that allow views to populate them.
    /// </summary>
    public class RegionNames
    {
        /// <summary>
        /// The region in which the toolbar populates.
        /// </summary>
        public static string ToolbarRegion = "ToolbarRegion";

        /// <summary>
        /// The region in which the content populates.
        /// </summary>
        public static string ContentRegion = "ContentRegion";
        
        /// <summary>
        /// The region in which the vehicle list populates.
        /// </summary>
        public static string VehicleListRegion = "VehicleListRegion";

        /// <summary>
        /// The region in which the maintenance checks populates.
        /// </summary>
        public static string ChecksRegion = "ChecksRegion";
        
        /// <summary>
        /// The region in which the petrol entries populates.
        /// </summary>
        public static string PetrolEntriesRegion = "PetrolEntriesRegion";

        /// <summary>
        /// The region in which the petrol history populates.
        /// </summary>
        public static string PetrolHistoryRegion = "PetrolHistoryRegion";
        
        /// <summary>
        /// The region in which the settings populates.
        /// </summary>
        public static string SettingsRegion = "SettingsRegion";
        
        /// <summary>
        /// The region in which the log settings populates.
        /// </summary>
        public static string LogSettingsRegion = "LogSettingsRegion";

        /// <summary>
        /// The region for displaying the sign in and create account views.
        /// </summary>
        public static string AuthenticateRegion = "AuthenticateRegion";

        public static string VehicleHeaderRegion = "VehicleHeaderRegion";
    }
}   //ImprezGarage.Infrastructure namespace 